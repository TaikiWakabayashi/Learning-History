/**
 * インデックスシグネチャ
 *
 * オブジェクトのプロパティを追加することができる
 */
interface Designer {
  name: string;
  /**
   * インデックスシグネチャの書き方
   *
   * []で囲い、プロパティ名を何型で書くか、そのプロパティが何型かを書く。
   *
   * ■注意点
   * インデックスシグネチャの[]の外の型を決めたら、
   * その他の全てのプロパティはその型で統一しなければいけない。
   */
  [index: string]: string;

  //   age: number;  コンパイルエラー
}

const designer: Designer = {
  name: "アンディーウォーホル",
  // 宣言とあっていれば自由に追加可能。
  role: "デザイナー",
  genre: "ファッション・アート",
};
/**
 * 呼び出す時も自由に追加できてしまうので、注意。
 */
console.log(designer.age);
